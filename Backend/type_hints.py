### Type Hints ###

my_string_variable = "My String Variable"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

my_typed_variable: str = "My typed String Variable"
#El editor al decirle que mi variable es de tipo string, cuando utilizamos el punto, seguido del nombre de mi variable me ayuda a elegir funciones de tipo string  ###my_typed_variable.###
print(my_typed_variable)
print(type(my_typed_variable))

